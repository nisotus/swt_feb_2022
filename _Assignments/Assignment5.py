# SIMPLE CALCULATOR
# Write a Python program that can perform addition, subtraction, multiplication and division based on two inputs from the user
# So the program should perform all above arithmetic operations on any set two numbers you pass to it.


#Solution


calculator1 = int(input("Enter the first number: "))
calculator2 = int(input("Enter the second number: "))

sum = calculator1 + calculator2

subtraction = calculator1 - calculator2

multiplication = calculator1 * calculator2
division = calculator1 / calculator2

print (sum)
print(subtraction)
print(multiplication)
print(division)

