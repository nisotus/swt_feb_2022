# What is the difference between "if" and "elif"
#    "If" always checks the first condition. "Elif" only checks if the preceding condition was false

# What is the benefit or advantage of using "else"
#   The code in the "else" block runs if the preceding condition(s) evaluate to false. It can be used as a final resort,
#   or something to do if none of the preceding conditions were met.
