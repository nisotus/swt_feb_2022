# Shortcuts for Windows Users

# Press "End" to move the cursor to the end of the line

# Press "Home" to move the cursor to the beginning of the line

# Press "Shift + Home" to move the cursor to the beginning of the file

# Press "Shift + End" to move the cursor to the end of the file

# Press "Alt + Up Arrow" to move a line or chunk of code up - Need to select the line/chunk of code first

# Press "Alt + Down Arrow" to move a line or chunk of code down - Need to select the line/chunk of code first

# Press "Shift + Alt + Up/Down Arrow" to duplocate line or chunk of code - Need to select the line/chunk of code first
