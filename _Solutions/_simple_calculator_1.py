n1 = int(input("Enter first number: "))
n2 = int(input("Enter second number: "))

print("The addition of the two number entered is: ", n1 + n2)
print("The subtraction of the two number entered is: ", n1 - n2)
print("The multiplication of the two number entered is: ", n1 * n2)
print("The division of the two number entered is: ", n1 / n2)