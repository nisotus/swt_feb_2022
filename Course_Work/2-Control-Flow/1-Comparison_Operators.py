# We use comparison operators to compare different values

# Below are some examples


# >
# <
# >=
# <=
# ==
# !=

print(bool(10 == "10"))
print(bool(10 > 3))
print(bool(10 < 20))
print(bool(10 <= 15))
print(bool(10 == 10))
print(bool("big" == "apple"))


print(bool("big" > "BIG"))

