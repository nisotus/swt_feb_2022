# Falsy values in Python - There are values or expressions that return "False"
# Empty string ("") is a Falsy and interpreted as a boolean false
# The digit 0 or Number 0 is a Falsy and interpreted as boolean False
# None: None represents the absence of a value. It is a Falsy and interpreted as a boolean False

# But whenever we use the values ("", 0, None) in a boolean context, we get a False. Anything else will be True