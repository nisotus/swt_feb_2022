# In Python we have 3 types of numbers

# integers
# float
# Complex

# For all the number types we have standard arithmetic operations that we have in MATHS, that we can perform on them

# addition
x = 20
y = 15
value = x + y
print(value)


# subtraction
x = 20
y = 15
value = x - y
print(value)


# division with remainder
x = 5
y = 2
value = x / y
print(value)


# division without remainder
x = 5
y = 2
value = x // y
print(value)


# multiplication
x = 4
y = 2
value = x * y
print(value)


# exponent
x = 4
y = 2
value = x ** y
print(value)

# modulus
x = 4
y = 2
value = x % y
print(value)


# Augmented Assignment Operator
a = 1 + 1
print(a)

a += 1 # a = a + 1

print(a)

b = 0 # b = 0
b += 3 # b = 3
b += 4 # b = 7

print(b)

# Complex number
x = 1 + 2j
print(x)